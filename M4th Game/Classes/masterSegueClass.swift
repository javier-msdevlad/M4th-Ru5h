import UIKit

class masterSegueClass: UIStoryboardSegue {

    override func perform() {
        
        
        let sourceID = self.source as! ViewControllerIdentityViewController
        let destinationID = self.destination as! ViewControllerIdentityViewController
        
        switch sourceID.viewIdentifier {
            
        case "mainMenu":
            switch destinationID.viewIdentifier {
                
            case "optionsMenu":
                toLeftBottomCorner()
                break
                
            case "aboutMenu":
                toBottom()
                break
                
            case "scoresMenu":
                toRightBottomCorner()
                break
            default:
                break
            }
            
        case "scoreView":
            switch destinationID.viewIdentifier {
            case "mainMenu":
                toRight()
                break
                
            case "playView":
                toLeft()
                break
                
            default:
                break
            }
            break
            
        
        case "playView":
            toRight()
            break
            
        case "optionsMenu":
            fromLeftBottomCorner()
            break
            
        case "aboutMenu":
            fromBottom()
            break
        
        case "scoresMenu":
            fromRightBottomCorner()
            break
            
        default:
            break
        }
    }
    
    func toLeftBottomCorner() {
       
        let src = self.source
        let dst = self.destination
        
        src.view.superview?.insertSubview(dst.view, aboveSubview: src.view)
        dst.view.transform = CGAffineTransform(translationX: -src.view.frame.size.width, y: src.view.frame.size.height)
        
        UIView.animate(withDuration: 0.25,
                       delay: 0.0,
                       options: .curveEaseInOut,
                       animations: {
                        dst.view.transform = CGAffineTransform(translationX: 0, y: 0)
        },
                       completion: { finished in
                        src.present(dst, animated: false, completion: nil)
        }
        )
        
    }
    
